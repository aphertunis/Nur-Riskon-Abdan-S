public class latihan18 {
  public static void main(String[] args) {
    // Inisialisasi variabel
    double angka = 3.5;

    // Loop untuk mencetak kelipatan 3.5 dari 3.5 hingga 35
    while (angka <= 35) {
      System.out.println(angka);
      angka += 3.5;  // Menambah angka dengan 3.5 pada setiap iterasi
    }
  }
}
